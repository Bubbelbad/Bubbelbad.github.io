class Bubble {

    constructor(name, description, image) {
        this.name = name;
        this.description = description;
        this.image = image;
    }

    getName() {
        return this.name;
    }

    getDescription() {
        return this.description;
    }

    getImage() {
        return this.image;
    }

    setName(name) {
        this.name = name;
    }

    setDescription(description) {
        this.description = description;
    }

    setImage(image) {
        this.image = image;
    }
}